package com.isi.axl.soap;

public class CmAxlInfoModel {
	
	private String cmType;
	private String 	cmIP;
	private int 	cmPort;
	private String 	cmID;
	private String 	cmPwd;
	
	public String getCmType() {
		return cmType;
	}
	public void setCmType(String cmType) {
		this.cmType = cmType;
	}
	public String getCmIP() {
		return cmIP;
	}
	public void setCmIP(String cmIP) {
		this.cmIP = cmIP;
	} 
	public int getCmPort() {
		return cmPort;
	}
	public void setCmPort(int cmPort) {
		this.cmPort = cmPort;
	}
	public String getCmID() {
		return cmID;
	}
	public void setCmID(String cmID) {
		this.cmID = cmID;
	}
	public String getCmPwd() {
		return cmPwd;
	}
	public void setCmPwd(String cmPwd) {
		this.cmPwd = cmPwd;
	}
	
	
	
}
